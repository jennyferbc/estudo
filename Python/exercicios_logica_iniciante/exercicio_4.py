#4Faça um Programa que peça as 4 notas bimestrais e mostre a média.

bimestre_1 = int(input("Informe a nota do Primeiro Bimestre "))
bimestre_2 = int(input("Informe a nota do Segundo Bimestre "))
bimestre_3 = int(input("Informe a nota do Terceiro Bimestre "))
bimestre_4 = int(input("Informe a nota do Quarto Bimestre "))

media = (bimestre_1 + bimestre_2 + bimestre_3 + bimestre_4) / 4

print("Sua média bimestral é: ", media)
